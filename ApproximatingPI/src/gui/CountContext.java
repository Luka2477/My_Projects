package gui;

public class CountContext {

    private double x, y;

    public double getX() {
        return this.x;
    }

    public void addX(double x) {
        this.x += x;
    }

    public double getY() {
        return this.y;
    }

    public void addY(double y) {
        this.y += y;
    }
}
